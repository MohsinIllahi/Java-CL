class switch6{
public static void main( String args[])
{
int courses = 2;
 
switch(courses){
case 1:
      System.out.println("java");
case 2:
      System.out.println("python");
case 3:
      System.out.println("C++");
case 4:
      System.out.println("AI");
case 5:
      System.out.println("ML");
case 6:
      System.out.println("AWS");
default:
      System.out.println("No course");
     }
   }
}